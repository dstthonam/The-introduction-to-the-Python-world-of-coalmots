# input() 함수를 통해 입력받은 값은 '문자열성격'의 데이터
국어 = int(input("국어성적을 입력해 주세요: "))
영어 = int(input("영어성적을 입력해 주세요: "))
수학 = int(input("수학성적을 입력해 주세요: "))
과학 = int(input("과학성적을 입력해 주세요: "))

총점 = 국어 + 영어 + 수학 + 과학
평균 = 총점/4

print("총점:", 총점)
print("평균:", 평균)

